class FairValueEstimator:
    """Estimates fair value using rolling median and mean reversion"""
    
    def __init__(self, window: int = 20):
        self.window = window
        self.mid_prices: deque = deque(maxlen=window)
        self.fair_value: Optional[float] = None
    
    def update(self, mid_price: float) -> float:
        """Update fair value estimate with new mid price"""
        self.mid_prices.append(mid_price)
        if len(self.mid_prices) >= 5:
            # Use median for robustness to outliers
            sorted_prices = sorted(self.mid_prices)
            n = len(sorted_prices)
            if n % 2 == 0:
                self.fair_value = (sorted_prices[n//2 - 1] + sorted_prices[n//2]) / 2
            else:
                self.fair_value = sorted_prices[n//2]
        return self.fair_value or mid_price
    
    def get_mean_reversion_signal(self) -> float:
        """
        Returns signal strength for mean reversion.
        Positive = current price below fair value (buy signal)
        Negative = current price above fair value (sell signal)
        """
        if not self.mid_prices or self.fair_value is None:
            return 0.0
        
        current = self.mid_prices[-1]
        deviation = (self.fair_value - current) / self.fair_value
        return deviation


class VolatilityEstimator:
    """Estimates recent price volatility"""
    
    def __init__(self, window: int = 10):
        self.window = window
        self.returns: deque = deque(maxlen=window)
        self.last_price: Optional[float] = None
    
    def update(self, price: float) -> float:
        """Update volatility estimate"""
        if self.last_price is not None and self.last_price > 0:
            ret = abs(price - self.last_price) / self.last_price
            self.returns.append(ret)
        self.last_price = price
        
        if len(self.returns) < 2:
            return 0.001  # Default low volatility
        
        # Average absolute return
        return sum(self.returns) / len(self.returns)


class InventoryManager:
    """Manages positions and applies inventory-based risk controls"""
    
    def __init__(self, max_position: int):
        self.position = Position(quantity=0, symbol="")
        self.max_position = max_position
        self.trades: list[Trade] = []
    
    def update_position(self, trade: Trade):
        """Update position when a trade executes"""
        qty = trade.quantity
        if trade.seller == "SUBMISSION":  # We sold
            self.position.quantity -= qty
            if self.position.quantity < 0:
                # Update avg entry for short
                prev_qty = abs(self.position.quantity) - qty
                if prev_qty > 0:
                    self.position.avg_entry_price = (
                        (self.position.avg_entry_price * prev_qty + trade.price * qty) / 
                        abs(self.position.quantity)
                    )
                else:
                    self.position.avg_entry_price = trade.price
        elif trade.buyer == "SUBMISSION":  # We bought
            self.position.quantity += qty
            if self.position.quantity > 0:
                # Update avg entry for long
                prev_qty = abs(self.position.quantity) - qty
                if prev_qty > 0:
                    self.position.avg_entry_price = (
                        (self.position.avg_entry_price * prev_qty + trade.price * qty) / 
                        self.position.quantity
                    )
                else:
                    self.position.avg_entry_price = trade.price
        
        self.trades.append(trade)
    
    def get_inventory_skew(self) -> float:
        """
        Returns skew factor based on current inventory.
        Positive = we're long, should lower bids/asks to reduce position
        Negative = we're short, should raise bids/asks to reduce position
        """
        if self.max_position == 0:
            return 0.0
        return self.position.quantity / self.max_position
    
    def can_buy(self, quantity: int) -> bool:
        return self.position.quantity + quantity <= self.max_position
    
    def can_sell(self, quantity: int) -> bool:
        return self.position.quantity - quantity >= -self.max_position


