"""
Prosperity 4 - Algorithmic Trading Bot v1.0
Strategy: Mean-Reversion Market Maker

This bot implements a market-making strategy that:
1. Places bid/ask orders to capture the spread
2. Uses mean-reversion to adjust fair value estimates
3. Manages inventory risk with position limits
4. Dynamically adjusts quotes based on market conditions
"""

import csv
import glob
from collections import deque
from dataclasses import dataclass, field
from typing import Optional


# ============================================================
# Configuration
# ============================================================
@dataclass
class Config:
    # General settings
    DATA_DIR: str = "TUTORIAL_ROUND_1"
    
    # EMERALDS configuration
    EMERALDS_FAIR_VALUE: float = 10000.0
    EMERALDS_SPREAD_TARGET: float = 8.0  # Half-spread from fair value
    EMERALDS_ORDER_SIZE: int = 8
    EMERALDS_MAX_POSITION: int = 30
    
    # TOMATOES configuration
    TOMATOES_FAIR_VALUE: float = 5000.0
    TOMATOES_SPREAD_TARGET: float = 7.0  # Half-spread from fair value
    TOMATOES_ORDER_SIZE: int = 5
    TOMATOES_MAX_POSITION: int = 25
    
    # Mean reversion settings
    MA_WINDOW: int = 20  # Rolling mean window for mid-price
    
    # Inventory skew settings
    INVENTORY_SKEW_FACTOR: float = 0.5  # How much to skew quotes based on position
    
    # Volatility adjustment
    VOL_LOOKBACK: int = 10
    VOL_SPREAD_MULTIPLIER: float = 1.5


