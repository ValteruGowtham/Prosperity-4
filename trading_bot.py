@dataclass
class OrderBookLevel:
    """Represents a single order book snapshot for a product"""
    day: int
    timestamp: int
    product: str
    
    # Bid side (prices people are willing to BUY at)
    bid_price_1: Optional[float] = None
    bid_volume_1: Optional[int] = None
    bid_price_2: Optional[float] = None
    bid_volume_2: Optional[int] = None
    bid_price_3: Optional[float] = None
    bid_volume_3: Optional[int] = None
    
    # Ask side (prices people are willing to SELL at)
    ask_price_1: Optional[float] = None
    ask_volume_1: Optional[int] = None
    ask_price_2: Optional[float] = None
    ask_volume_2: Optional[int] = None
    ask_price_3: Optional[float] = None
    ask_volume_3: Optional[int] = None
    
    mid_price: Optional[float] = None


@dataclass
class Trade:
    """Represents an executed trade"""
    timestamp: int
    buyer: str
    seller: str
    symbol: str
    price: float
    quantity: int


@dataclass
class Position:
    """Tracks current position for a product"""
    symbol: str
    quantity: int = 0  # Positive = long, Negative = short
    avg_entry_price: float = 0.0
    realized_pnl: float = 0.0


@dataclass
class Signal:
    """Trading signal generated by the strategy"""
    symbol: str
    action: str  # "BUY", "SELL", "HOLD"
    price: float
    quantity: int
    confidence: float  # 0.0 to 1.0


