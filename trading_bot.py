# Backtesting Engine
# ============================================================
class Backtester:
    """Runs the strategy on historical data"""
    
    def __init__(self, strategy: MarketMakingStrategy):
        self.strategy = strategy
    
    def run(self, prices: list[OrderBookLevel], trades: list[Trade]) -> dict:
        """Run backtest on historical data"""
        trade_idx = 0
        signals_generated = 0
        
        for book in prices:
            # Process any trades that occurred at this timestamp
            while trade_idx < len(trades) and trades[trade_idx].timestamp <= book.timestamp:
                self.strategy.process_trade(trades[trade_idx])
                trade_idx += 1
            
            # Generate signal
            signal = self.strategy.process_orderbook(book)
            if signal:
                self.strategy.signals.append(signal)
                signals_generated += 1
        
        return self.strategy.get_summary()


