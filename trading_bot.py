class DataLoader:
    """Loads and parses market data from CSV files"""
    
    def __init__(self, data_dir: str):
        self.data_dir = data_dir
    
    def load_prices(self) -> list[OrderBookLevel]:
        """Load all price data from CSV files"""
        prices = []

        # Find all price files
        price_files = glob.glob(self.data_dir + "/prices_*.csv")

        for filepath in sorted(price_files):
            with open(filepath, 'r') as f:
                reader = csv.DictReader(f, delimiter=';')
                for row in reader:
                    level = OrderBookLevel(
                        day=int(row['day']),
                        timestamp=int(row['timestamp']),
                        product=row['product'],
                        bid_price_1=self._safe_float(row.get('bid_price_1')),
                        bid_volume_1=self._safe_int(row.get('bid_volume_1')),
                        bid_price_2=self._safe_float(row.get('bid_price_2')),
                        bid_volume_2=self._safe_int(row.get('bid_volume_2')),
                        bid_price_3=self._safe_float(row.get('bid_price_3')),
                        bid_volume_3=self._safe_int(row.get('bid_volume_3')),
                        ask_price_1=self._safe_float(row.get('ask_price_1')),
                        ask_volume_1=self._safe_int(row.get('ask_volume_1')),
                        ask_price_2=self._safe_float(row.get('ask_price_2')),
                        ask_volume_2=self._safe_int(row.get('ask_volume_2')),
                        ask_price_3=self._safe_float(row.get('ask_price_3')),
                        ask_volume_3=self._safe_int(row.get('ask_volume_3')),
                        mid_price=self._safe_float(row.get('mid_price')),
                    )
                    prices.append(level)
        
        # Sort by day, timestamp, then product
        prices.sort(key=lambda x: (x.day, x.timestamp, x.product))
        return prices
    
    def load_trades(self) -> list[Trade]:
        """Load all trade data from CSV files"""
        trades = []

        # Find all trade files
        trade_files = glob.glob(self.data_dir + "/trades_*.csv")

        for filepath in sorted(trade_files):
            with open(filepath, 'r') as f:
                reader = csv.DictReader(f, delimiter=';')
                for row in reader:
                    if row.get('symbol'):  # Skip empty rows
                        trade = Trade(
                            timestamp=int(row['timestamp']),
                            buyer=row.get('buyer', ''),
                            seller=row.get('seller', ''),
                            symbol=row['symbol'],
                            price=float(row['price']),
                            quantity=int(row['quantity']),
                        )
                        trades.append(trade)
        
        trades.sort(key=lambda x: x.timestamp)
        return trades
    
    @staticmethod
    def _safe_float(val) -> Optional[float]:
        if val and val.strip():
            return float(val)
        return None
    
    @staticmethod
    def _safe_int(val) -> Optional[int]:
        if val and val.strip():
            return int(val)
        return None


