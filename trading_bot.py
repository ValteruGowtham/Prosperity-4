# Main Strategy
# ============================================================
class MarketMakingStrategy:
    """
    Mean-Reversion Market Making Strategy v1.0
    
    This strategy:
    1. Estimates fair value using rolling median
    2. Places bid/ask orders around fair value
    3. Skews quotes based on inventory
    4. Widens spread during high volatility
    """
    
    def __init__(self, config: Config):
        self.config = config
        
        # Per-product state
        self.fair_value_estimators = {
            "EMERALDS": FairValueEstimator(config.MA_WINDOW),
            "TOMATOES": FairValueEstimator(config.MA_WINDOW),
        }
        self.volatility_estimators = {
            "EMERALDS": VolatilityEstimator(config.VOL_LOOKBACK),
            "TOMATOES": VolatilityEstimator(config.VOL_LOOKBACK),
        }
        self.inventory_managers = {
            "EMERALDS": InventoryManager(config.EMERALDS_MAX_POSITION),
            "TOMATOES": InventoryManager(config.TOMATOES_MAX_POSITION),
        }
        
        # Trading log
        self.signals: list[Signal] = []
        self.order_log: list[dict] = []
    
    def process_orderbook(self, book: OrderBookLevel) -> Optional[Signal]:
        """Process a single order book snapshot and generate trading signal"""
        product = book.product
        
        if product not in self.fair_value_estimators:
            return None
        
        # Update estimators
        mid_price = book.mid_price or ((book.bid_price_1 + book.ask_price_1) / 2 if book.bid_price_1 and book.ask_price_1 else None)
        if mid_price is None:
            return None
        
        fair_value = self.fair_value_estimators[product].update(mid_price)
        volatility = self.volatility_estimators[product].update(mid_price)
        inventory_skew = self.inventory_managers[product].get_inventory_skew()
        
        # Get product-specific config
        if product == "EMERALDS":
            base_spread = self.config.EMERALDS_SPREAD_TARGET
            order_size = self.config.EMERALDS_ORDER_SIZE
            fair_value_default = self.config.EMERALDS_FAIR_VALUE
        else:  # TOMATOES
            base_spread = self.config.TOMATOES_SPREAD_TARGET
            order_size = self.config.TOMATOES_ORDER_SIZE
            fair_value_default = self.config.TOMATOES_FAIR_VALUE
        
        # Use default fair value if estimator hasn't converged
        if fair_value is None:
            fair_value = fair_value_default
        
        # Adjust spread based on volatility
        vol_adjustment = 1.0 + (volatility * self.config.VOL_SPREAD_MULTIPLIER * 100)
        adjusted_spread = base_spread * vol_adjustment
        
        # Calculate optimal bid/ask with inventory skew
        # If we're long (positive inventory), lower our quotes to sell
        # If we're short (negative inventory), raise our quotes to buy
        skew_adjustment = inventory_skew * self.config.INVENTORY_SKEW_FACTOR * adjusted_spread
        
        optimal_bid = fair_value - adjusted_spread + skew_adjustment
        optimal_ask = fair_value + adjusted_spread + skew_adjustment
        
        # Ensure we maintain minimum spread
        if optimal_ask - optimal_bid < 2:
            midpoint = (optimal_bid + optimal_ask) / 2
            optimal_bid = midpoint - 1
            optimal_ask = midpoint + 1
        
        # Determine action based on current market vs our quotes
        current_bid = book.bid_price_1
        current_ask = book.ask_price_1
        
        if current_bid is None or current_ask is None:
            return None
        
        # Check if we should place/update orders
        signal = self._generate_signal(
            product=product,
            optimal_bid=optimal_bid,
            optimal_ask=optimal_ask,
            current_bid=current_bid,
            current_ask=current_ask,
            order_size=order_size,
        )
        
        # Log the decision
        self.order_log.append({
            "timestamp": book.timestamp,
            "product": product,
            "fair_value": round(fair_value, 2),
            "mid_price": round(mid_price, 2),
            "volatility": round(volatility, 6),
            "inventory_skew": round(inventory_skew, 4),
            "optimal_bid": round(optimal_bid, 2),
            "optimal_ask": round(optimal_ask, 2),
            "current_bid": round(current_bid, 2),
            "current_ask": round(current_ask, 2),
            "signal": signal.action if signal else "HOLD",
            "signal_price": round(signal.price, 2) if signal else 0,
        })
        
        return signal
    
    def _generate_signal(
        self,
        product: str,
        optimal_bid: float,
        optimal_ask: float,
        current_bid: float,
        current_ask: float,
        order_size: int,
    ) -> Optional[Signal]:
        """Generate trading signal based on optimal vs current prices"""
        manager = self.inventory_managers[product]
        
        # Mean reversion signal strength
        fair_value = (optimal_bid + optimal_ask) / 2
        mid_price = (current_bid + current_ask) / 2
        mean_rev_strength = abs(fair_value - mid_price) / fair_value
        
        # Only trade if deviation is significant
        if mean_rev_strength < 0.0005:  # 0.05% threshold
            return None
        
        if fair_value > mid_price and manager.can_buy(order_size):
            # Price below fair value - BUY signal
            # Place bid at optimal price
            return Signal(
                symbol=product,
                action="BUY",
                price=round(optimal_bid, 2),
                quantity=order_size,
                confidence=min(mean_rev_strength * 100, 1.0),
            )
        elif fair_value < mid_price and manager.can_sell(order_size):
            # Price above fair value - SELL signal
            # Place ask at optimal price
            return Signal(
                symbol=product,
                action="SELL",
                price=round(optimal_ask, 2),
                quantity=order_size,
                confidence=min(mean_rev_strength * 100, 1.0),
            )
        
        return None
    
    def process_trade(self, trade: Trade):
        """Process an executed trade and update positions"""
        if trade.symbol in self.inventory_managers:
            self.inventory_managers[trade.symbol].update_position(trade)
    
    def get_summary(self) -> dict:
        """Get strategy performance summary"""
        summary = {
            "total_signals": len([s for s in self.signals if s]),
            "buy_signals": len([s for s in self.signals if s and s.action == "BUY"]),
            "sell_signals": len([s for s in self.signals if s and s.action == "SELL"]),
        }
        
        for product, manager in self.inventory_managers.items():
            summary[f"{product}_position"] = manager.position.quantity
            summary[f"{product}_trades"] = len(manager.trades)
        
        return summary


